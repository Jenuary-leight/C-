#include<iostream>

using namespace std;

void sw(int a[3][3])
{
	swap(a[0][0],a[0][1]);
	swap(a[1][0],a[1][1]);
	swap(a[2][0],a[2][1]);
	for(int i=0;i<3;i++)
	{
		for(int j=0;j<3;j++)
		{
			cout<<a[i][j]<<" ";
		}
		cout<<endl;
	}
}
int main()
{
	int a[3][3]=
	{
		{5,7,8},
		{6,5,4},
		{3,2,1},
	 }; 
	sw(a);
	
	return 0;
}
